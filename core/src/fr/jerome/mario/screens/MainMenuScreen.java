package fr.jerome.mario.screens;

import com.badlogic.gdx.Gdx;
import com.badlogic.gdx.Screen;
import com.badlogic.gdx.graphics.GL20;
import com.badlogic.gdx.graphics.OrthographicCamera;

import fr.jerome.mario.Mario;

/**
 * Menu principal du jeux !
 * Created by jerome on 01/10/14.
 */
public class MainMenuScreen implements Screen {

    final Mario game;
    OrthographicCamera camera;

    public MainMenuScreen(final Mario gam, OrthographicCamera cam) {

        game = gam;
        camera = cam;
    }

    @Override
    public void render(float delta) {
        Gdx.gl.glClearColor(0, 0, 0, 1);
        Gdx.gl.glClear(GL20.GL_COLOR_BUFFER_BIT);


        game.batch.begin();
        game.font.draw(game.batch, "Welcome to the new (old) Supe Mario Bros !!! ", 100, 150);
        game.font.draw(game.batch, "Tap anywhere to begin!", 100, 100);
        game.batch.end();

        if (Gdx.input.isTouched()) {
            game.setScreen(new GameScreen(game));
            dispose();
        }
    }

    @Override
    public void resize(int width, int height) {

    }

    @Override
    public void show() {

    }

    @Override
    public void hide() {

    }

    @Override
    public void pause() {

    }

    @Override
    public void resume() {

    }

    @Override
    public void dispose() {

    }
}
